    private void loadData() {
        pegawaiService = new PegawaiImplement();
        List<Pegawai> listPegawai = new ArrayList<>();
        listPegawai = pegawaiService.findAll();
        Object[][] objectPegawai = new Object[listPegawai.size()][6];
        
        int counter = 0;
        for (Pegawai pegawai : listPegawai) {
            objectPegawai[counter][0] = pegawai.getIdPegawai();
            objectPegawai[counter][1] = pegawai.getNamaPegawai();
            objectPegawai[counter][2] = pegawai.isStatus();
            counter++;
        }
        tbl_pegawai.setModel(new javax.swing.table.DefaultTableModel(
            objectPegawai,
            new String [] {
                "ID", "Nama", "Status"
            }
        ));
    }
    
    private void loadData(Pegawai pegawai) {
        Object[][] objectPegawai = new Object[1][6];
        
        objectPegawai[0][0] = pegawai.getIdPegawai();
        objectPegawai[0][1] = pegawai.getNamaPegawai();
        objectPegawai[0][2] = pegawai.isStatus();
            
        tbl_pegawai.setModel(new javax.swing.table.DefaultTableModel(
            objectPegawai,
            new String [] {
                "ID", "Nama", "Status",
            }
        ));
    }
    
    private Pegawai findPegawai(String id) {
        Pegawai pegawai = new Pegawai();
        pegawaiService = new PegawaiImplement();
        pegawai = pegawaiService.findById(id);
    
        return pegawai;
    }
