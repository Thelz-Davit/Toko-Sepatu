    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        String nama, status,id;
        
        pegawaiService = new PegawaiImplement();
        
        id = (txt_id.getText());
        nama = txt_nama.getText();
        status = cmbStatus.getSelectedItem().toString();
        
        
        Pegawai pegawai = new Pegawai();
        pegawai.setIdPegawai(id);
        pegawai.setNamaPegawai(nama);
        pegawai.isStatus();


        pegawaiService.update(pegawai);
        JOptionPane.showMessageDialog(null, "Data pegawai updated successfully");
        loadData();
        emptyField();
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        String id;
        pegawaiService = new PegawaiImplement();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        
        id = (txt_id.getText());
        
        int dialogResult = JOptionPane.showConfirmDialog (null, "Are you sure to delete it?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            pegawaiService.delete(id);
            loadData();
            emptyField();
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

